//Gunakan operator + pada tipe data String dan tipe data Int
fun main () {
    //Deklarasi Variabel
    var firstWord = "SMK"
    var middleWord = " Telkom "
    var lastWord = "Purwokerto"

    //Deklarasi Pemanggilan Variabel
    println(firstWord + middleWord + lastWord)

    //Deklarasi Variabel
    var panjang = 43
    var lebar = 2
    val luas: Int = panjang * lebar

    //Deklarasi Pemanggilan Variabel
    println("Luas persegi panjang " + (luas))
}